const n1 = document.querySelector('#n1')
const n2 = document.querySelector('#n2') 
const res = document.querySelector('#res')  

function somar(){
res.innerHTML = parseInt(n1.value) + parseInt(n2.value)
} 
function subtrair(){
res.innerHTML = parseInt(n1.value) - parseInt(n2.value)
} 
    
function multiplicar(){
res.innerHTML = parseInt(n1.value) * parseInt(n2.value)
} 
    
function dividir(){
res.innerHTML = parseInt(n1.value) - parseInt(n2.value)
} 